namespace Krypton.Toolkit;

/// <summary>
/// Specifies a boolean that can inherit its value.
/// </summary>
public enum InheritBool
{
    /// <summary>
    /// Specifies the value should be inherited.
    /// </summary>
    Inherit,

    /// <summary>
    /// Specifies a boolean true.
    /// </summary>
    True,

    /// <summary>
    /// Specifies a boolean false.
    /// </summary>
    False
}