namespace Krypton.Toolkit;

/// <summary>
/// Specifies a palette element.
/// </summary>
public enum PaletteElement
{
    /// <summary>
    /// Specifies the track of a track bar.
    /// </summary>
    TrackBarTrack,

    /// <summary>
    /// Specifies the tick of a track bar.
    /// </summary>
    TrackBarTick,

    /// <summary>
    /// Specifies the position marker of a track bar.
    /// </summary>
    TrackBarPosition
}